     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = User::where('role', '!=', 'admin');

        if (request('search')) {
            $users->where('nama_lengkap', 'like', '%' . request('search') . '%')->orWhere('email', 'like', '%' . request('search') . '%');
        }

        return view('profile', [
            "users" => $users->get(),
            "active" => "data-profile"
        ]);
    }

